package durgaSoft;

public class looping {
	//naveen

	public static void main(String[] args) {
		int count = 0;
		//while loop
		int i = 1;//initialization part
		while(i <= 10) { //conditional part
		System.out.print(" " + i);
		i = i + 1; //incremental / decrement part
		count++;
	}
		System.out.println();
		System.out.println(" number of count: " + count);
	
	
	//for loop
	for(int j = 1; j <= 10; j++) {
		System.out.print(" " + j);
		}
	}
}